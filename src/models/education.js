class Education {

  name;
  city;
  degree;
  subject;
  graduateDate;
  gpa;

  constructor(name, city, degree, subject, graduateDate, gpa) {
    this.name = name;
    this.city = city;
    this.degree = degree;
    this.subject = subject;
    this.graduateDate = graduateDate;
    this.gpa = gpa;
  }
}